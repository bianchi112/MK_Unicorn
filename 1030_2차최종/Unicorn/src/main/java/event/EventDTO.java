package event;

public class EventDTO {
	private String eventTilte;
	private String eventDetails;
	private String eventDate;
	private String eventImgUrl;
	public String getEventTilte() {
		return eventTilte;
	}
	public void setEventTilte(String eventTilte) {
		this.eventTilte = eventTilte;
	}
	public String getEventDetails() {
		return eventDetails;
	}
	public void setEventDetails(String eventDetails) {
		this.eventDetails = eventDetails;
	}
	public String getEventDate() {
		return eventDate;
	}
	public void setEventDate(String eventDate) {
		this.eventDate = eventDate;
	}
	public String getEventImgUrl() {
		return eventImgUrl;
	}
	public void setEventImgUrl(String eventImgUrl) {
		this.eventImgUrl = eventImgUrl;
	}
	
}
